//! `SYS`/`SYSL` named-instruction alias directory.
//!
//! The architectural `SYS #op1, Cn, Cm, #op2{, Xt}` (and the `L==1` `SYSL`)
//! encoding has a large directory of *named* aliases — `IC`/`DC`/`AT`/`TLBI`
//! and the newer `PLBI`/`GIC`/`GICR`/`MLBI`/`APAS`/`TRCIT`/`COSP` families — each
//! a thin rename of a specific `(op1, CRn, CRm, op2)` tuple plus an operation
//! keyword (`ivau`, `zva`, `vae1is`, ...). This single flat table is the source
//! of truth for *both* directions: the decoder looks an entry up by tuple, the
//! encoder by `(mnemonic, op-name)`. Keeping one table guarantees the two stay
//! in lockstep.
//!
//! The table is derived by sweeping the whole `SYS`/`SYSL` `(op1,CRn,CRm,op2)`
//! sub-space through the LLVM oracle, so it mirrors LLVM's preferred disassembly
//! exactly. The `CFP`/`CPP`/`DVP` (`rctx`) aliases and the `GCS`/`TENTER`/hint
//! ops are *not* here — those are distinct instructions handled inline in
//! [`crate::decode::branch_sys`].

use crate::mnemonic::Mnemonic;

/// One named `SYS`/`SYSL` alias: the `(op1, CRn, CRm, op2)` selector, the alias
/// mnemonic, the operation keyword (`""` for the keyword-less `APAS`/`TRCIT`),
/// and the operand-shape flags.
#[derive(Clone, Copy)]
pub(crate) struct SysInsEntry {
    /// `op1` (`word<18:16>`).
    pub op1: u8,
    /// `CRn` (`word<15:12>`).
    pub crn: u8,
    /// `CRm` (`word<11:8>`).
    pub crm: u8,
    /// `op2` (`word<7:5>`).
    pub op2: u8,
    /// `true` for the `SYSL` read side (`L==1`, e.g. `GICR`).
    pub read: bool,
    /// The alias mnemonic.
    pub mnem: Mnemonic,
    /// The operation keyword (e.g. `"vae1is"`), or `""` when the form has none.
    pub name: &'static str,
    /// `true` if the form prints an `<Xt>` transfer register.
    pub needs_rt: bool,
    /// `true` if the keyword precedes the register (`gic vden, x0`); `false` for
    /// the register-first read forms (`gicr x0, cdia`).
    pub kw_first: bool,
}

/// Compact constructor used by the table literal below.
#[allow(clippy::too_many_arguments)]
const fn e(
    op1: u8,
    crn: u8,
    crm: u8,
    op2: u8,
    read: u8,
    mnem: Mnemonic,
    name: &'static str,
    needs_rt: bool,
    kw_first: bool,
) -> SysInsEntry {
    SysInsEntry {
        op1,
        crn,
        crm,
        op2,
        read: read != 0,
        mnem,
        name,
        needs_rt,
        kw_first,
    }
}

/// Look up a named alias by its `(read, op1, CRn, CRm, op2)` selector.
#[inline]
pub(crate) fn lookup(read: bool, op1: u32, crn: u32, crm: u32, op2: u32) -> Option<&'static SysInsEntry> {
    let (op1, crn, crm, op2) = (op1 as u8, crn as u8, crm as u8, op2 as u8);
    let mut i = 0;
    while i < SYS_INS_TABLE.len() {
        let t = &SYS_INS_TABLE[i];
        if t.read == read && t.op1 == op1 && t.crn == crn && t.crm == crm && t.op2 == op2 {
            return Some(t);
        }
        i += 1;
    }
    None
}

/// Look up a named alias by its `(mnemonic, op-name)` — the encoder direction.
/// Matches on the mnemonic family and the operation keyword; `APAS`/`TRCIT`
/// (no keyword) match on the mnemonic alone.
#[inline]
pub(crate) fn lookup_by_name(mnem: Mnemonic, name: &str) -> Option<&'static SysInsEntry> {
    let mut i = 0;
    while i < SYS_INS_TABLE.len() {
        let t = &SYS_INS_TABLE[i];
        if t.mnem as u16 == mnem as u16 && str_eq(t.name, name) {
            return Some(t);
        }
        i += 1;
    }
    None
}

/// Byte-wise `&str` equality (avoids any non-`const` machinery).
#[inline]
fn str_eq(a: &str, b: &str) -> bool {
    a.as_bytes() == b.as_bytes()
}

